          <h5>History Of FUOYE</h5>
          <img src="../assets/fuoyelof.webp" alt="School Logo">
          <h4><p>The pioneer Vice-Chancellor, Federal University, Oye-Ekiti, Professor Chinedu Nebo, was recently appointed
            Minister of Power by the President of the Federal Republic of Nigeria, His Excellency, Dr. Goodluck Ebele
            Jonathan, GCFR. Consequently, Professor Isaac Uzoma Asuzu was appointed Vice Chancellor of FUOYE in his place.
          </p><br>

          <p> The new Vice Chancellor arrived FUOYE on Wednesday, February 27th 2013. As a very courteous citizen, and with
            a burning zeal to have a first-hand assessment of the task ahead, embarked on courtesy visits of the campuses
            of the University as well as the traditional rulers of the host communities of Oye and Ikole. </p> <br>

           <p>The journey started as early as 10am with the first port of call being the Oloye’s Palace located within the
            Oye town. On the Vice Chancellor’s entourage were the Ag. Registrar, Mr. Daniel Adeyemo, The Dean, Faculty of
            Engineering, Professor Chris Bolu, the University Librarian, Mr. Michael Afolabi, the Director, Information
            and Protocol, Dr. Ojiaku Kalu, and head of Personnel, Mr Ayodele Fatuuase. Of course, the VC was accompanied
            on the visit by his beautiful wife, Mrs Chiwe Asuzu. </p> <br>

          <p>  The visitors were received on arrival at the Oloye’s Palace by a band of jubilant palce drummers as well as
            the Oba in Council led by no less a personality than the Oloye himself, Oba Sir Michael Oluwole Ademolaju,
            OON. Also present were the wife of then Kabiyesi, some high chiefs, executive members of the Oye Progressive
            Union, the Priest in charge of the Oye Catholic Church, Rev. Assumpta Ajayi, as well as women and other
            opinion leaders of the town.</p> <br>

           <p> The Ag. Registrar introduced the Vice Chancellor and those on his entourage, while Chief Agbaje Sunday, former
            President, Oye Progressive Union, (OPU) introduced the Oloye-In-Council. In his welcome address, OPU leader
            praised the outgone VC in glowing terms. He described the former VC as a forthright and diligent administrator
            and wished him success in his present assignment as the Minister for Power. He, in welcoming the new VC,
            implored him to build on the existing strong foundation laid by his predecessor, Prof. Nebo and to regularly
            confer with the Royalty for counsel. He also advised the VC never to enter into any commitment with anybody or
            group claiming to represent the community on any matter whatsoever, except in direct consultation with, and
            express consent of the Oba. The Community leadership assured him and the University of their continued
            support.</p> <br>

           <p> Responding, Prof. Asuzu said his respect for the traditional institution as well as need to foster good
            landlord –tenant relationship informed his early call to the palace. He said he needed the blessing of the Oba
            and his Council to make a success of his assignment at the University and promised to regularly visit and
            confer with the Oba as needs arise. He, however, outlined the main business of the University which is to
            promote academics and said education is the instrument that brings progress in any community, while asserting
            that there is much work to be done at the University which invariably requires the support of the Oba, and the
            community as a whole. His dream, he said, was to see FUOYE ranked first among universities in Nigeria in a
            very short while.</p> <br>

          <p>  His Royal Highness, in receiving his visitors, confessed his belief that Prof Asuzu has actually come and
            ready for work. He said he arrived at this conviction based on the fact that the VC had cone with his wife as
            he did not want anything to distract his focus on developing the young University. He canvassed for the
            confirmation of the Entrepreneurship Training Scheme initiated by the out-gone VC and informed his guests that
            Oye-Ekiti is the largest producer of plantain and banana which he hoped will assist the Agriculture Department
            of the university.</p> <br>

           <p> The Oloye advised staff and students of the university to always carry their IDs for security reasons while
            promising that the community would organize a befitting send forth for Prof. Nebo and that same event would
            also serve as formal reception for the new VC. According to the Royal father, at the proposed event, the key
            to the Oye town would be officially handed over to the VC. He said the doors of his Palace were open to
            receive members of the University management. The ceremony was rounded off with a closing prayer for the CV
            and staff of the University.</p> <br>

           <p> Same day, the team left for the Place of Elekole of Ikole where the Vice Chancellor and other principal
            officers of the University were received by all the traditional rulers in the Ikole Local Government Area. The
            visit actually coincided with the statutory monthly meeting of Ikole Local Government Council of Obas.</p> <br>

           <p>In his welcome speech, the Elekole of Ikole, Oba Adewumi Ajibade Fasiku, said he and his community offer the
            Vice Chancellor their warmest regards, saying the visitors are profoundly received. He said the Ikole people
            believed in peace and hated cheating. He said Prof. Chinedu Nebo did his job with his sixth sense by carrying
            the two host communities along without rancor. He saw the quick visit of the new top manas towing that same
            line, assuring him of the co-operation of the community in particular. The royal father prayed for the good
            health of the VC as well as the grace to achieve much to enhance the status of the university. Concluding, Oba
            Adesiku urged landlords and taxi drivers in his domain to support the university to succeed by being
            reasonable in their charges for rents and services to the university staff and students as well as other
            visitors as proof of his commitment to peaceful co-existence of Town and Gown. </p> <br>

          <p>  Responding, the VC said it was necessary that he made the courtesy call to inform the Kabiyesi that he had
            arrived at his domain. His objective as the vice-chancellor, he said, was to train the students well enough to
            be both competitive and employable anywhere. He therefore solicited the support of the Elekole In-Council for
            the success of the university and also canvassed for continued peace as a panacea for progress.</p> <br>

          <p>> There were delegations from the Oha-na-Eze Ndi Igbo as well as the Hausa Community in the Ikole Kingdom who
            came to receive the new administrator and his team at the Palace. The Kabiyesi later took the Vice Chancellor
            and his team on a guided tour of the palace. The tour terminated at his private office where the VC signed the
            visitor’s register. Group photographs were taken to immortalize the visit. </p><br>

           <p> From there, the team left for the Ikole Campus of the University where it was received by the Head of Campus,
            Professor Adeolu Aganga. Prof Isaac Asuzu was taken round the university and he inspected a lot of buildings,
            offices and laboratories. Some observations were made and views were also taken from staff while definite
            instructions were given in some instances. The ICT center at the Ikole Campus, a state of the art e-Centre,
            established as an integral part of the main ICT centre, with its independent premises now known as the
            Ikechukwu Iheanacho ICT centre named in memory of Arc. Iyke Ihenaacho, FUOYE’s pioneer ICT Director, who died
            in active service at the age of 41. It is among the best ICT centres in Nigeria universities with futuristic
            facilities.</p> <br>

            At the Health Centre, demand and challenges were mentioned and noted. Some of the challenges include lack of
            portable water and mosquito nets at their windows. At the Hostels, issue of security of student was raised by
            the VC, as he was not comfortable with the nearness of bush to the buildings which lacked perimeter fencing.
            From there, the team returned to the main campus where it took a guided tour of the faculties and facilities.
